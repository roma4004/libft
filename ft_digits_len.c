#include "libft.h"

size_t	ft_digits_len(const char *s)
{
	size_t	i;
	size_t	len;

	i = 0;
	len = 0;
	while (s[i++])
		if (s[i] >= '0' && s[i] <= '9')
			len++;
	return (len);
}
