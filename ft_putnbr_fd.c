#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	if (n > 2147483647 || n < -2147483648)
		return ;
	if (n < 0 && (n *= -1))
		ft_putchar_fd('-', fd);
	if (n == -2147483648)
	{
		ft_putchar_fd('2', fd);
		n = (n % 1000000000) * -1;
	}
	if (n >= 10)
	{
		ft_putnbr_fd(n / 10, fd);
		ft_putnbr_fd(n % 10, fd);
	}
	else
		ft_putchar_fd((char)(n + '0'), fd);
}
