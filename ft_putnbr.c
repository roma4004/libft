#include "libft.h"

void	ft_putnbr(int n)
{
	if (n > 2147483647 || n < -2147483648)
		return ;
	if (n == -2147483648)
	{
		ft_putstr("-2147483648");
		return ;
	}
	if (n < 0)
	{
		ft_putchar('-');
		n = -n;
	}
	if (n < 10)
	{
		ft_putchar((char)(n + '0'));
		return ;
	}
	ft_putnbr(n / 10);
	ft_putchar((char)(n % 10 + '0'));
	return ;
}
